from aiogram import Router
from aiogram.filters import Command
from aiogram.types import Message

router = Router()


@router.message(Command("start", "help"))
async def cmd_start(message: Message):
    text = (
        "🏦 <b>Банковская система RP</b>\n\n"
        "<b>Команды для всех:</b>\n"
        "/баланс — проверить баланс\n"
        "/перевести @user сумма — перевести деньги\n"
        "/история — история операций\n"
        "/запросить_кредит сумма — запросить кредит\n"
        "/запросить_вклад сумма — запросить вклад\n\n"
        "<b>Команды админов группы:</b>\n"
        "/начислить @user сумма — начислить деньги\n"
        "/списать @user сумма — списать деньги\n"
        "/одобрить_кредит id — одобрить кредит\n"
        "/отклонить_кредит id — отклонить кредит\n"
        "/одобрить_вклад id — одобрить вклад\n"
        "/отклонить_вклад id — отклонить вклад\n\n"
        "<i>Бот автоматически даёт админ-команды тем, у кого есть права администратора в группе.</i>\n\n"
        "💡 <i>В меню (по /) команды на английском, но русские тоже работают. "
        "Пиши как удобно.</i>"
    )
    await message.reply(text, parse_mode="HTML")
